#define LOGI(...) __android_log_print(ANDROID_LOG_INFO, "MuLang", __VA_ARGS__)

static MuLanguage g_lang = LANG_CN;
static MuLangCallback g_langCb = nullptr;

void muSetLanguage(MuLanguage lang) {
    if (lang != g_lang) {
        g_lang = lang;
        LOGI("Language switched to %s", lang == LANG_EN ? "English" : "中文");
        if (g_langCb) g_langCb(lang);
    }
}

MuLanguage muGetLanguage() { return g_lang; }
void muSetLangCallback(MuLangCallback cb) { g_langCb = cb; }

